package Chapter17;

public class Exercise15 {
    private int list;
    private int scores;
    private int age;

    public Exercise15(int list, int scores, int age) {
        this.list = list;
        this.scores = scores;
        this.age = age;
    }

    public int getList() {
        return list;
    }

    public void setList(int list) {
        this.list = list;
    }

    public int getScores() {
        return scores;
    }

    public void setScores(int scores) {
        this.scores = scores;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
